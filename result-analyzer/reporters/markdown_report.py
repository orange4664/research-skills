#!/usr/bin/env python3
"""
Markdown Report Generator — Generates a structured reproduction report.
Also outputs JSON for integration with latex-paper-skills and beamer-skill.
"""
import json
import os
from typing import Dict, List, Optional
from datetime import datetime


def generate_markdown_report(
    paper_title: str,
    metric_results: Optional[Dict] = None,
    curve_results: Optional[Dict] = None,
    image_results: Optional[List[Dict]] = None,
    extra_notes: Optional[str] = None,
    output_dir: str = '.',
) -> str:
    """
    Generate a comprehensive Markdown reproduction report.

    Returns the path to the generated report.
    """
    os.makedirs(output_dir, exist_ok=True)
    report_path = os.path.join(output_dir, 'reproduction_report.md')

    lines = []

    # Header
    lines.append(f'# Reproduction Report')
    lines.append(f'')
    lines.append(f'**Paper**: {paper_title}')
    lines.append(f'**Date**: {datetime.now().strftime("%Y-%m-%d %H:%M")}')
    lines.append(f'**Generated by**: result-analyzer')
    lines.append(f'')

    # ═══════════════════════════════════════════════════════════
    # Summary
    # ═══════════════════════════════════════════════════════════
    if metric_results:
        summary = metric_results.get('summary', {})
        overall = summary.get('overall_status', 'N/A')
        emoji = summary.get('overall_emoji', '')

        lines.append(f'## {emoji} Overall: **{overall}**')
        lines.append(f'')
        lines.append(f'- Metrics compared: **{summary.get("total", 0)}**')
        lines.append(f'- Passed: **{summary.get("passed", 0)}**')
        if summary.get('warned', 0) > 0:
            lines.append(f'- Warnings: **{summary.get("warned", 0)}**')
        if summary.get('failed', 0) > 0:
            lines.append(f'- Failed: **{summary.get("failed", 0)}**')
        lines.append(f'')

    # ═══════════════════════════════════════════════════════════
    # Metric Comparison Table
    # ═══════════════════════════════════════════════════════════
    if metric_results and metric_results.get('comparisons'):
        lines.append(f'## Metric Comparison')
        lines.append(f'')
        lines.append(f'| Status | Metric | Paper | Reproduced | Diff | Note |')
        lines.append(f'|--------|--------|-------|-----------|------|------|')

        for comp in metric_results['comparisons']:
            paper_val = f'{comp["paper"]:.4g}' if comp['paper'] is not None else 'N/A'
            repro_val = f'{comp["reproduced"]:.4g}' if comp['reproduced'] is not None else 'N/A'
            lines.append(
                f'| {comp["emoji"]} {comp["status"]} | {comp["metric"]} | '
                f'{paper_val} | {repro_val} | '
                f'{comp["diff_str"]} | {comp["note"]} |'
            )

        lines.append(f'')

    # ═══════════════════════════════════════════════════════════
    # Training Curves
    # ═══════════════════════════════════════════════════════════
    if curve_results:
        lines.append(f'## Training Curves')
        lines.append(f'')

        # Embed plot if it exists
        figures_dir = os.path.join(output_dir, 'figures')
        plot_path = os.path.join(figures_dir, 'training_curves.png')
        if os.path.exists(plot_path):
            rel_path = os.path.relpath(plot_path, output_dir)
            lines.append(f'![Training Curves]({rel_path})')
            lines.append(f'')

        if 'final_value_comparisons' in curve_results:
            lines.append(f'### Final Values')
            lines.append(f'')
            for comp in curve_results['final_value_comparisons']:
                lines.append(f'- **{comp["metric"]}**: {comp["reproduced"]:.4g} '
                            f'(paper: {comp["paper"]:.4g}, '
                            f'diff: {comp["diff_str"]}) {comp["emoji"]}')
            lines.append(f'')

        if isinstance(curve_results, dict) and 'correlation' in curve_results:
            lines.append(f'- Pearson correlation: **{curve_results["correlation"]:.3f}**')
            lines.append(f'- Trend match: {"Yes" if curve_results.get("trend_match") else "No"}')
            lines.append(f'')

    # ═══════════════════════════════════════════════════════════
    # Image Comparison
    # ═══════════════════════════════════════════════════════════
    if image_results:
        lines.append(f'## Image Comparison')
        lines.append(f'')

        for img_result in image_results:
            lines.append(f'### {os.path.basename(img_result.get("reproduced", ""))}')

            if img_result.get('ssim') is not None:
                lines.append(f'- SSIM: **{img_result["ssim"]:.4f}** ({img_result.get("ssim_grade", "")})')
            if img_result.get('psnr') is not None:
                lines.append(f'- PSNR: **{img_result["psnr"]:.2f}** dB')
            if img_result.get('fid') is not None:
                lines.append(f'- FID: **{img_result["fid"]:.2f}**')
            if img_result.get('status'):
                lines.append(f'- Status: {img_result["emoji"]} {img_result["status"]}')
            lines.append(f'')

        # Embed side-by-side if exists
        figures_dir = os.path.join(output_dir, 'figures')
        side_by_side_path = os.path.join(figures_dir, 'sample_comparison.png')
        if os.path.exists(side_by_side_path):
            rel_path = os.path.relpath(side_by_side_path, output_dir)
            lines.append(f'![Side-by-side Comparison]({rel_path})')
            lines.append(f'')

    # ═══════════════════════════════════════════════════════════
    # Notes
    # ═══════════════════════════════════════════════════════════
    if extra_notes:
        lines.append(f'## Notes')
        lines.append(f'')
        lines.append(extra_notes)
        lines.append(f'')

    # ═══════════════════════════════════════════════════════════
    # Next Steps
    # ═══════════════════════════════════════════════════════════
    lines.append(f'## Next Steps')
    lines.append(f'')
    lines.append(f'To generate a presentation from this report:')
    lines.append(f'```bash')
    lines.append(f'# Use beamer-skill to create a PPT')
    lines.append(f'# Input: this report\'s JSON output')
    lines.append(f'beamer-skill: create a reproduction report presentation from reproduction_report.json')
    lines.append(f'```')
    lines.append(f'')
    lines.append(f'To integrate into a LaTeX paper:')
    lines.append(f'```bash')
    lines.append(f'# Use latex-paper-skills results-backfill')
    lines.append(f'# Input: reproduction_report.json → paper/results/')
    lines.append(f'```')
    lines.append(f'')

    # Write
    report_content = '\n'.join(lines)
    with open(report_path, 'w', encoding='utf-8') as f:
        f.write(report_content)

    return report_path


def generate_json_report(
    paper_title: str,
    metric_results: Optional[Dict] = None,
    curve_results: Optional[Dict] = None,
    image_results: Optional[List[Dict]] = None,
    output_dir: str = '.',
) -> str:
    """
    Generate a structured JSON report.
    This can be consumed by:
    - latex-paper-skills (results-backfill)
    - beamer-skill (PPT generation)
    """
    os.makedirs(output_dir, exist_ok=True)
    report_path = os.path.join(output_dir, 'reproduction_report.json')

    report = {
        'paper_title': paper_title,
        'generated_at': datetime.now().isoformat(),
        'generator': 'result-analyzer',
        'metrics': metric_results,
        'curves': _make_serializable(curve_results),
        'images': image_results,
        'summary': metric_results.get('summary', {}) if metric_results else {},
    }

    with open(report_path, 'w', encoding='utf-8') as f:
        json.dump(report, f, indent=2, ensure_ascii=False, default=str)

    return report_path


def generate_beamer_data(
    paper_title: str,
    metric_results: Optional[Dict] = None,
    curve_results: Optional[Dict] = None,
    image_results: Optional[List[Dict]] = None,
    output_dir: str = '.',
) -> Dict:
    """
    Generate structured data for beamer-skill to create a reproduction PPT.

    Returns a dict that beamer-skill can consume to create slides:
    - Title slide
    - Metric comparison slide (table)
    - Training curves slide (figure)
    - Sample comparison slide (figure)
    - Conclusion slide (pass/fail)
    """
    slides = []

    # Title slide
    overall = metric_results.get('summary', {}) if metric_results else {}
    slides.append({
        'type': 'title',
        'title': f'Reproduction Report: {paper_title}',
        'subtitle': f'Overall: {overall.get("overall_emoji", "")} {overall.get("overall_status", "N/A")}',
    })

    # Metrics table slide
    if metric_results and metric_results.get('comparisons'):
        table_rows = []
        for comp in metric_results['comparisons']:
            if comp['status'] not in ('MISSING', 'EXTRA'):
                table_rows.append({
                    'metric': comp['metric'],
                    'paper': f'{comp["paper"]:.4g}' if comp['paper'] is not None else 'N/A',
                    'reproduced': f'{comp["reproduced"]:.4g}' if comp['reproduced'] is not None else 'N/A',
                    'diff': comp['diff_str'],
                    'status': f'{comp["emoji"]} {comp["status"]}',
                })
        slides.append({
            'type': 'table',
            'title': 'Metric Comparison',
            'headers': ['Metric', 'Paper', 'Reproduced', 'Diff', 'Status'],
            'rows': table_rows,
        })

    # Training curves slide
    figures_dir = os.path.join(output_dir, 'figures')
    curves_fig = os.path.join(figures_dir, 'training_curves.png')
    if os.path.exists(curves_fig):
        slides.append({
            'type': 'figure',
            'title': 'Training Curves',
            'figure_path': curves_fig,
        })

    # Sample comparison slide
    sample_fig = os.path.join(figures_dir, 'sample_comparison.png')
    if os.path.exists(sample_fig):
        slides.append({
            'type': 'figure',
            'title': 'Generated Samples',
            'figure_path': sample_fig,
        })

    # Conclusion slide
    slides.append({
        'type': 'conclusion',
        'title': 'Conclusion',
        'overall_status': overall.get('overall_status', 'N/A'),
        'passed': overall.get('passed', 0),
        'total': overall.get('total', 0),
    })

    beamer_data_path = os.path.join(output_dir, 'beamer_report_data.json')
    with open(beamer_data_path, 'w', encoding='utf-8') as f:
        json.dump({'slides': slides}, f, indent=2, ensure_ascii=False, default=str)

    return {'slides': slides, 'data_path': beamer_data_path}


def _make_serializable(obj):
    """Make numpy/other objects JSON serializable."""
    if obj is None:
        return None
    if isinstance(obj, dict):
        return {k: _make_serializable(v) for k, v in obj.items()}
    if isinstance(obj, list):
        return [_make_serializable(v) for v in obj]
    try:
        import numpy as np
        if isinstance(obj, (np.integer,)):
            return int(obj)
        if isinstance(obj, (np.floating,)):
            return float(obj)
        if isinstance(obj, np.ndarray):
            return obj.tolist()
    except ImportError:
        pass
    return obj
