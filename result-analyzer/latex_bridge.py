#!/usr/bin/env python3
"""
LaTeX Paper Skills Bridge — Convert result-analyzer JSON output
into the format expected by latex-paper-skills results-backfill.

This bridge connects:
  result-analyzer → reproduction_report.json
  ↓
  latex-paper-skills → paper/results/*.csv + paper/results/*.tex

Usage:
    python latex_bridge.py reproduction_report.json -o paper/results/

What it generates:
    paper/results/
    ├── main_results.csv        # Metric comparison table (CSV)
    ├── main_results.tex        # LaTeX table (ready for \\input{})
    └── reproduction_summary.tex # Summary block for abstract/conclusion
"""
import json
import os
import sys
import argparse
from typing import Dict, List, Optional


def json_to_csv(report_path: str, output_dir: str) -> str:
    """
    Convert reproduction_report.json to CSV format.
    Compatible with latex-paper-skills results-backfill.
    """
    with open(report_path, 'r', encoding='utf-8') as f:
        report = json.load(f)

    metrics = report.get('metrics', {})
    comparisons = metrics.get('comparisons', []) if metrics else []

    if not comparisons:
        print("  ⚠️ No metric comparisons found in report")
        return ''

    os.makedirs(output_dir, exist_ok=True)
    csv_path = os.path.join(output_dir, 'main_results.csv')

    with open(csv_path, 'w', encoding='utf-8') as f:
        f.write('metric,paper_value,reproduced_value,absolute_diff,relative_diff,status\n')
        for comp in comparisons:
            if comp.get('paper') is None or comp.get('reproduced') is None:
                continue
            f.write(
                f"{comp['metric']},"
                f"{comp['paper']},"
                f"{comp['reproduced']},"
                f"{comp.get('diff_abs', '')},"
                f"{comp.get('diff_rel', '')},"
                f"{comp.get('status', '')}\n"
            )

    print(f"  ✅ CSV: {csv_path}")
    return csv_path


def json_to_latex_table(report_path: str, output_dir: str) -> str:
    """
    Convert reproduction_report.json to a LaTeX table.
    Ready for \\input{results/main_results.tex} in a paper.
    """
    with open(report_path, 'r', encoding='utf-8') as f:
        report = json.load(f)

    metrics = report.get('metrics', {})
    comparisons = metrics.get('comparisons', []) if metrics else []

    if not comparisons:
        return ''

    os.makedirs(output_dir, exist_ok=True)
    tex_path = os.path.join(output_dir, 'main_results.tex')

    lines = []
    lines.append(r'% Auto-generated by result-analyzer latex_bridge.py')
    lines.append(r'% \input{results/main_results.tex}')
    lines.append(r'\begin{table}[h]')
    lines.append(r'\centering')
    lines.append(r'\caption{Reproduction Results Comparison}')
    lines.append(r'\label{tab:reproduction_results}')
    lines.append(r'\begin{tabular}{lcccc}')
    lines.append(r'\toprule')
    lines.append(r'\textbf{Metric} & \textbf{Paper} & \textbf{Reproduced} & \textbf{Diff} & \textbf{Status} \\')
    lines.append(r'\midrule')

    for comp in comparisons:
        if comp.get('paper') is None or comp.get('reproduced') is None:
            continue

        metric = comp['metric'].replace('_', r'\_')
        paper_val = f"{comp['paper']:.4g}"
        repro_val = f"{comp['reproduced']:.4g}"

        diff_abs = comp.get('diff_abs', 0)
        sign = '+' if diff_abs > 0 else ''
        diff_str = f"{sign}{diff_abs:.4g}"

        status = comp.get('status', '')
        if status == 'PASS':
            status_tex = r'\textcolor{green!60!black}{\checkmark}'
        elif status == 'WARN':
            status_tex = r'\textcolor{orange}{$\sim$}'
        elif status == 'FAIL':
            status_tex = r'\textcolor{red}{$\times$}'
        else:
            status_tex = status

        # Bold the best value
        if status in ('PASS',):
            repro_val = rf'\textbf{{{repro_val}}}'

        lines.append(f'{metric} & {paper_val} & {repro_val} & {diff_str} & {status_tex} \\\\')

    lines.append(r'\bottomrule')
    lines.append(r'\end{tabular}')
    lines.append(r'\end{table}')

    with open(tex_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines) + '\n')

    print(f"  ✅ LaTeX table: {tex_path}")
    return tex_path


def json_to_summary_block(report_path: str, output_dir: str) -> str:
    """
    Generate a LaTeX text block summarizing reproduction results.
    Can be inserted into abstract or conclusion.
    """
    with open(report_path, 'r', encoding='utf-8') as f:
        report = json.load(f)

    summary = report.get('summary', {})
    title = report.get('paper_title', 'the paper')
    metrics = report.get('metrics', {})
    comparisons = metrics.get('comparisons', []) if metrics else []

    os.makedirs(output_dir, exist_ok=True)
    tex_path = os.path.join(output_dir, 'reproduction_summary.tex')

    # Build summary text
    total = summary.get('total', 0)
    passed = summary.get('passed', 0)
    overall = summary.get('overall_status', 'N/A')

    # Find key metrics for the text
    key_results = []
    for comp in comparisons:
        if comp.get('status') in ('PASS', 'WARN') and comp.get('paper') is not None:
            metric = comp['metric']
            repro = comp['reproduced']
            paper = comp['paper']
            key_results.append(f"{metric} of {repro:.4g} (vs. {paper:.4g} reported)")

    results_text = ', '.join(key_results[:3]) if key_results else 'comparable performance'

    text = (
        f"% Auto-generated reproduction summary\n"
        f"% Insert into abstract or conclusion with \\input{{results/reproduction_summary.tex}}\n"
        f"We successfully reproduced the key results of {title.replace('_', ' ')}, "
        f"achieving {results_text}. "
        f"Out of {total} metrics compared, {passed} were within the specified tolerance, "
        f"yielding an overall reproduction status of \\textbf{{{overall}}}.\n"
    )

    with open(tex_path, 'w', encoding='utf-8') as f:
        f.write(text)

    print(f"  ✅ Summary block: {tex_path}")
    return tex_path


def main():
    parser = argparse.ArgumentParser(
        description='Convert result-analyzer JSON to latex-paper-skills format'
    )
    parser.add_argument('report_json', help='Path to reproduction_report.json')
    parser.add_argument('-o', '--output-dir', default='paper/results',
                        help='Output directory (default: paper/results/)')

    args = parser.parse_args()

    if not os.path.exists(args.report_json):
        print(f"Error: {args.report_json} not found")
        sys.exit(1)

    print(f"  📄 Converting: {args.report_json}")
    print(f"  📂 Output dir: {args.output_dir}")

    json_to_csv(args.report_json, args.output_dir)
    json_to_latex_table(args.report_json, args.output_dir)
    json_to_summary_block(args.report_json, args.output_dir)

    print(f"\n  ✨ Done! Files ready for latex-paper-skills results-backfill")
    print(f"     Use: \\input{{results/main_results.tex}} in your paper")


if __name__ == '__main__':
    main()
