    parser.add_argument('--describe', help='Text description of the paper/model')
    parser.add_argument('-o', '--output', required=True, help='Output directory')
    parser.add_argument('--framework', choices=['pytorch', 'tensorflow', 'jax'],
                        default='pytorch')

    args = parser.parse_args()

    if args.paper:
        paper_data = extract_paper_info(args.paper)
    elif args.markdown:
        paper_data = extract_from_markdown(args.markdown)
    elif args.describe:
        paper_data = {
            'title': args.describe,
            'sections': [],
            'references': [],
            'full_text': args.describe,
        }
    else:
        parser.print_help()
        return

