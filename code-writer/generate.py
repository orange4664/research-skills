def generate_project(paper_data: dict, output_dir: str, framework: str = "pytorch"):
    """
    Generate a project scaffolding from extracted paper information.

    Args:
        paper_data: Extracted paper info (from extract_paper_info)
        output_dir: Directory to write generated project
        framework: Target framework (pytorch/tensorflow/jax)
    """
    os.makedirs(output_dir, exist_ok=True)
    os.makedirs(os.path.join(output_dir, "src"), exist_ok=True)
    os.makedirs(os.path.join(output_dir, "configs"), exist_ok=True)
    os.makedirs(os.path.join(output_dir, "references"), exist_ok=True)

    # Extract all info
    arch = extract_architecture(
        paper_data.get('sections', []),
        paper_data.get('full_text', '')
    )
    exp = extract_experiment(
        paper_data.get('sections', []),
        paper_data.get('full_text', '')
    )
    refs = find_reference_code(paper_data)

    title = paper_data.get('title', 'UnnamedModel')
    model_name = arch.get('model_name', '') or _title_to_class_name(title)

    hp = exp.get('hyperparameters', {})

    # ── Generate config ──
    config = {
        'model': {
            'name': model_name,
            'architecture': arch.get('architecture_type', 'unknown'),
            'components': arch.get('components', []),
        },
        'training': {
            'learning_rate': float(hp.get('learning_rate', '1e-3')),
            'batch_size': int(hp.get('batch_size', '32')),
            'epochs': int(hp.get('epochs', '100')),
            'optimizer': hp.get('optimizer', 'Adam'),
            'weight_decay': float(hp.get('weight_decay', '0')),
            'seed': int(hp.get('seed', '42')),
        },
        'data': {
            'datasets': exp.get('datasets', []),
            'input_size': 32,
        },
        'evaluation': {
            'metrics': exp.get('metrics', []),
        },
    }

    with open(os.path.join(output_dir, "configs", "default.yaml"), 'w', encoding='utf-8') as f:
        import yaml
        yaml.dump(config, f, default_flow_style=False, allow_unicode=True)

    # ── Generate model.py ──
    _write_model_py(output_dir, model_name, arch, title)

    # ── Generate train.py ──
    _write_train_py(output_dir, model_name, config['training'])

    # ── Generate data.py ──
    _write_data_py(output_dir, model_name, config)

    # ── Generate requirements.txt ──
    _write_requirements(output_dir, framework)

    # ── Generate README ──
    _write_readme(output_dir, title, model_name, config)

    # ── Generate reference discovery plan ──
    _write_reference_plan(output_dir, refs)

    # ── Generate implementation checklist ──
    _write_checklist(output_dir, title, arch, exp)

    # Print summary
    print(f"\n{'═' * 60}")
    print(f"  📂 Project generated: {output_dir}")
    print(f"  🏷️  Model: {model_name}")
    print(f"  🔧 Framework: {framework}")
    print(f"  🧱 Components: {', '.join(arch.get('components', ['unknown']))}")
    print(f"  📊 Datasets: {', '.join(exp.get('datasets', ['not specified']))}")
    print(f"  📐 Equations found: {len(paper_data.get('equations', []))}")
    print(f"  🔗 Base methods: {', '.join(refs.get('base_methods', ['none']))}")
    print(f"{'═' * 60}")
    print(f"\n  Next steps:")
    print(f"  1. Read references/search_plan.md and find reference code")
    print(f"  2. Fill in TODO markers in src/model.py")
    print(f"  3. Implement data loading in src/data.py")
    print(f"  4. Run: cd {output_dir} && pip install -r requirements.txt")
    print(f"  5. Test: python src/train.py --epochs 1")


def _title_to_class_name(title: str) -> str:
    """Convert paper title to a Python class name."""
    words = title.split()[:3]
    name = ''.join(w.capitalize() for w in words if w.isalpha())
    return name or 'Model'


